/* = is as assignment operator */

let a = 10;

console.log(a);

/* this transaltes to 
   a = a + 10;
*/

a += 10; 

/* this is post increment
   the value of a is used and
   then incremented
*/

console.log(a++); // This prints 20

/* this is pre increment
   the value of a is incremented and
   then used
*/

console.log(++a); // This prints 22

